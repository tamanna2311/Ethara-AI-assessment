"""
CRUD stands for Create, Read, Update, and Delete.
This file contains all the helper functions that actually perform these actions on the database.
Think of it as the "instruction manual" for interacting with the database.
When a router receives a request (e.g., "Give me all products"), it calls a function in this file to do the heavy lifting.
"""

# 1. Importing necessary components
# ---------------------------------------------------------
# 'Session' is the database connection object. We need it to run queries.
from sqlalchemy.orm import Session

# 'IntegrityError' is a specific error that happens when we violate a database rule.
# For example, if we try to create a user with an email that already exists, 
# and the database was told emails must be unique, it throws this error.
from sqlalchemy.exc import IntegrityError

# 'HTTPException' is used to send standard web errors back to the frontend.
# E.g., Error 404 (Not Found) or 400 (Bad Request).
from fastapi import HTTPException

# We import our own models (database tables) and schemas (data structures).
from . import models, schemas


# ==========================================
# PRODUCT CRUD OPERATIONS
# ==========================================

def get_product(db: Session, product_id: int):
    """
    Reads a single product from the database using its ID.
    It asks the database: "Query the Product table, filter by ID, and give me the first match."
    """
    return db.query(models.Product).filter(models.Product.id == product_id).first()

def get_product_by_sku(db: Session, sku: str):
    """
    Reads a single product using its unique SKU (Stock Keeping Unit).
    """
    return db.query(models.Product).filter(models.Product.sku == sku).first()

def get_products(db: Session, skip: int = 0, limit: int = 100):
    """
    Reads a list of products.
    'skip' and 'limit' are used for pagination. 
    E.g., if skip=10 and limit=10, it gives us products 11 through 20.
    """
    return db.query(models.Product).offset(skip).limit(limit).all()

def create_product(db: Session, product: schemas.ProductCreate):
    """
    Takes data from the user (schemas.ProductCreate), converts it into a database row (models.Product),
    and saves it to the database.
    """
    # 1. Convert the Pydantic schema into a dictionary, then spread it into the SQLAlchemy model.
    db_product = models.Product(**product.model_dump())
    
    # 2. Add the new row to the database session.
    db.add(db_product)
    
    try:
        # 3. Commit (save) the changes permanently.
        db.commit()
        
        # 4. Refresh updates our Python object with any new data generated by the database (like the auto-generated ID).
        db.refresh(db_product)
        return db_product
    except IntegrityError:
        # If the SKU already exists, SQLAlchemy throws an IntegrityError.
        # We roll back the transaction so the database doesn't get stuck in an error state.
        db.rollback()
        # We tell the frontend: 400 Bad Request - SKU already exists.
        raise HTTPException(status_code=400, detail="SKU already exists")

def update_product(db: Session, product_id: int, product: schemas.ProductUpdate):
    """
    Finds an existing product and changes its details.
    """
    # 1. Get the current product from the database.
    db_product = get_product(db, product_id)
    if not db_product:
        # If it doesn't exist, return None (the router will handle sending a 404 error).
        return None
        
    # 2. Get the new data provided by the user. 
    # 'exclude_unset=True' means we only update fields the user ACTUALLY sent. 
    # If they didn't send a new price, we don't touch the price.
    update_data = product.model_dump(exclude_unset=True)
    
    # 3. Loop through the provided data and update the database object.
    for key, value in update_data.items():
        setattr(db_product, key, value)
        
    try:
        # 4. Save the changes
        db.commit()
        db.refresh(db_product)
        return db_product
    except IntegrityError:
        db.rollback()
        raise HTTPException(status_code=400, detail="SKU already exists")

def delete_product(db: Session, product_id: int):
    """
    Finds a product and removes it from the database.
    """
    db_product = get_product(db, product_id)
    if db_product:
        db.delete(db_product)
        db.commit()
    return db_product


