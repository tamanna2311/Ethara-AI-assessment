# ==========================================
# ORDER CRUD OPERATIONS
# ==========================================

def get_order(db: Session, order_id: int):
    return db.query(models.Order).filter(models.Order.id == order_id).first()

def get_orders(db: Session, skip: int = 0, limit: int = 100):
    return db.query(models.Order).offset(skip).limit(limit).all()

def create_order(db: Session, order: schemas.OrderCreate):
    """
    Creating an order is complex because it affects multiple tables: 
    It creates an Order, creates OrderItems, AND reduces Product stock.
    """
    
    # 1. Ensure the customer who is placing the order actually exists.
    db_customer = get_customer(db, order.customer_id)
    if not db_customer:
        raise HTTPException(status_code=404, detail="Customer not found")

    # Variables to keep track of the total price and the items being ordered.
    total_amount = 0.0
    order_items = []
    
    # 2. Loop through every item the user is trying to buy.
    for item in order.items:
        # Check if the product exists
        db_product = get_product(db, item.product_id)
        if not db_product:
            raise HTTPException(status_code=404, detail=f"Product with id {item.product_id} not found")
        
        # IMPORTANT: Check if we have enough stock to fulfill this item.
        if db_product.stock_quantity < item.quantity:
            raise HTTPException(status_code=400, detail=f"Insufficient stock for product '{db_product.name}'")
        
        # Deduct the ordered quantity from our inventory stock.
        db_product.stock_quantity -= item.quantity
        
        # Add the cost of these items to the total bill.
        total_amount += db_product.price * item.quantity
        
        # Create an OrderItem database record (but we haven't saved it yet).
        order_items.append(
            models.OrderItem(product_id=item.product_id, quantity=item.quantity)
        )
    
    # 3. Create the main Order record with the calculated total amount and items.
    db_order = models.Order(
        customer_id=order.customer_id,
        total_amount=total_amount,
        items=order_items
    )
    
    # 4. Save everything to the database.
    # Note: Because of how SQLAlchemy works, adding the `db_order` automatically adds the `order_items` too.
    # Also, because we haven't committed yet, if ANYTHING failed above (like out of stock), 
    # NO changes are saved to the database. It's an all-or-nothing transaction.
    db.add(db_order)
    db.commit()
    db.refresh(db_order)
    
    return db_order

def delete_order(db: Session, order_id: int):
    """
    Deletes an order.
    Because we set `cascade="all, delete-orphan"` in models.py, 
    deleting an order will automatically delete all the associated OrderItems too!
    """
    db_order = get_order(db, order_id)
    if db_order:
        db.delete(db_order)
        db.commit()
    return db_order
